static void glyph_draw_rows(SDL_Renderer *renderer, float x, float y, float pixel, const uint8_t rows[7], SDL_Color color)
{
  SDL_SetRenderDrawColor(renderer, color.r, color.g, color.b, color.a);
  for (int row = 0; row < 7; row++) {
    uint8_t bits = rows[row];
    for (int col = 0; col < 5; col++) {
      if ((bits >> (4 - col)) & 1) {
        SDL_FRect r;
        r.x = x + (float)col * pixel;
        r.y = y + (float)row * pixel;
        r.w = pixel;
        r.h = pixel;
        SDL_RenderFillRectF(renderer, &r);
      }
    }
  }
}

void letterlock_draw_char(SDL_Renderer *renderer, float x, float y, float pixel, char ch, SDL_Color color)
{
  if (ch >= 'a' && ch <= 'z') {
    ch = (char)(ch - 'a' + 'A');
  }
  if (ch < 'A' || ch > 'Z') {
    return;
  }
  glyph_draw_rows(renderer, x, y, pixel, kLetter5x7[ch - 'A'], color);
}

void letterlock_draw_hidden_slot(SDL_Renderer *renderer, float x, float y, float pixel, SDL_Color color)
{
  SDL_SetRenderDrawColor(renderer, color.r, color.g, color.b, color.a);
  float w = 5.0f * pixel;
  float yy = y + 7.0f * pixel - pixel * 0.9f;
  SDL_FRect r;
  r.x = x;
  r.y = yy;
  r.w = w;
  r.h = pixel * 0.35f;
  if (r.h < 1.0f) {
    r.h = 1.0f;
  }
  SDL_RenderFillRectF(renderer, &r);
}
